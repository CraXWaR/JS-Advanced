function factory(lib, object) {
    const result = []

    for (const order of object) {
        const object = {}
        const template = order.template

        for (const pop in template) {
            object[pop] = template[pop]
        }
        const parts = order.parts;

        for (let part of parts) {
            object[part] = lib[part]
        }
        result.push(object)
    }
    return result;
}
